// One-time generator: extract the worker initDb() DDL (`const tables = [...]`)
// into migrations/0001_baseline.sql.
//
// The DDL in services/worker/src/lib/db.ts is pure static SQL (no `${}`
// interpolation), so the array of template literals can be evaluated safely
// and emitted as a flat .sql file. Re-run this whenever the baseline needs to
// be regenerated from the current worker schema (Phase 0 only — once the
// migration runner owns DDL, new changes go into new migrations/NNNN_*.sql).
//
//   node scripts/extract-baseline.mjs

import { readFileSync, writeFileSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const dbFile = path.join(root, "services/worker/src/lib/db.ts");
const outFile = path.join(root, "migrations/0001_baseline.sql");

const src = readFileSync(dbFile, "utf8");

const marker = "const tables = [";
const start = src.indexOf(marker);
if (start < 0) throw new Error("`const tables = [` not found in db.ts");

// The array closes at a 2-space-indented `];` (the inner SQL is indented
// deeper, and no inner line is exactly `\n  ];`).
const bracketStart = start + marker.length - 1; // index of '['
const closeRel = src.indexOf("\n  ];", bracketStart);
if (closeRel < 0) throw new Error("array close `\\n  ];` not found");
const bracketEnd = closeRel + "\n  ]".length; // index just past ']'

const arrayLiteral = src.slice(bracketStart, bracketEnd); // "[ `...`, `...` ]"

// Guard: no JS interpolation allowed (would break static SQL extraction).
if (/\$\{/.test(arrayLiteral)) {
  throw new Error("Found `${` interpolation in DDL — cannot emit static SQL.");
}

// Evaluate the (pure) array of template-literal strings.
const statements = new Function("return " + arrayLiteral)();
if (!Array.isArray(statements) || statements.length === 0) {
  throw new Error("Extraction produced an empty/invalid array.");
}

// The worker `tables` array interleaves ALTER/INDEX before some CREATE TABLEs
// (works in prod only because tables already exist). For a FRESH-DB-safe
// baseline, stable-partition so every `CREATE TABLE` runs first (relative
// order preserved → FK parent/child order intact), then all other statements
// in their original relative order (preserves the merge-before-unique-index
// and DO-block ordering). All statements are idempotent (IF NOT EXISTS).
const norm = (s) => String(s).trim().replace(/;?\s*$/, ";");
const isCreateTable = (s) => /^\s*CREATE TABLE IF NOT EXISTS/i.test(s);
const ordered = [
  ...statements.filter(isCreateTable),
  ...statements.filter((s) => !isCreateTable(s)),
];

const header = `-- 0001_baseline.sql
-- AUTO-GENERATED by scripts/extract-baseline.mjs from
-- services/worker/src/lib/db.ts (initDb). Do not edit by hand.
--
-- Baseline = the current production schema, captured as idempotent DDL
-- (CREATE/ALTER ... IF NOT EXISTS). Safe to run against an existing DB
-- (no-op) and to build a fresh DB from scratch.
--
-- NOTE: CREATE TABLE statements are hoisted to the top (stable order) so the
-- baseline applies cleanly on a FRESH database. The source array in db.ts
-- interleaves some ALTERs before their CREATE (which only worked because prod
-- already had the tables). See scripts/extract-baseline.mjs.
-- Statements: ${ordered.length}

`;

const body = ordered.map(norm).join("\n\n");

writeFileSync(outFile, header + body + "\n", "utf8");
console.log(`Wrote ${outFile} (${statements.length} statements).`);
