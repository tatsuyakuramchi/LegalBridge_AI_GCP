// Phase 1 (A2): generate migrations/0003_seed_templates.sql from the current
// file-based templates, importing them as document_templates + version 1.
//
// Sources:
//   templates_config.json            → label / category / _comment / vars
//   services/worker/templates/*.html → html_source (kind='document')
//   services/worker/templates/partials/*.html → html_source (kind='partial')
//
// `vars` (insertion-ordered object) is converted to an ordered array
// [{name, label, group, type, required, readonly, hidden, options, placeholder,
//   helpText, dbField}] to preserve field order in field_schema (JSONB).
//
// The emitted SQL is idempotent: head rows are upserted, version 1 is inserted
// only if absent, and current_version_id is (re)linked at the end.
//
//   node scripts/seed-templates.mjs

import { readFileSync, writeFileSync, readdirSync, existsSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const configPath = path.join(root, "templates_config.json");
const tplDir = path.join(root, "services/worker/templates");
const partialsDir = path.join(tplDir, "partials");
const outFile = path.join(root, "migrations/0003_seed_templates.sql");

const config = JSON.parse(readFileSync(configPath, "utf8"));

// Pick a dollar-quote tag that does not appear in the given text.
function dq(text, base) {
  let tag = `$${base}$`;
  let n = 0;
  while (text.includes(tag)) tag = `$${base}${++n}$`;
  return tag;
}
function quote(text, base) {
  const tag = dq(text ?? "", base);
  return `${tag}${text ?? ""}${tag}`;
}
function sqlStr(s) {
  if (s === null || s === undefined) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

// vars object -> ordered array of field defs (name first).
function fieldSchema(vars) {
  if (!vars || typeof vars !== "object") return [];
  return Object.keys(vars).map((name) => ({ name, ...vars[name] }));
}

const out = [];
out.push("-- 0003_seed_templates.sql");
out.push("-- AUTO-GENERATED by scripts/seed-templates.mjs. Do not edit by hand.");
out.push("-- Imports file-based templates into document_templates + version 1 (idempotent).");
out.push("");

let docCount = 0;
let partialCount = 0;
const warnings = [];

function emitTemplate({ key, kind, label, category, comment, html, schema }) {
  const htmlQ = quote(html, "tpl");
  const schemaJson = JSON.stringify(schema ?? []);
  const schemaQ = quote(schemaJson, "json");
  out.push(`-- ${kind}: ${key}`);
  out.push(`WITH t AS (
  INSERT INTO document_templates (template_key, kind, label, category, comment)
  VALUES (${sqlStr(key)}, ${sqlStr(kind)}, ${sqlStr(label)}, ${sqlStr(category)}, ${sqlStr(comment)})
  ON CONFLICT (template_key) DO UPDATE
    SET label = EXCLUDED.label, category = EXCLUDED.category,
        comment = EXCLUDED.comment, kind = EXCLUDED.kind, updated_at = now()
  RETURNING id
)
INSERT INTO document_template_versions (template_id, version_no, html_source, field_schema, comment, created_by)
SELECT t.id, 1, ${htmlQ}, ${schemaQ}::jsonb, 'initial import (Phase 1 / A2)', 'seed-templates'
FROM t
ON CONFLICT (template_id, version_no) DO NOTHING;`);
  out.push("");
}

// 1) document templates (config-driven)
for (const key of Object.keys(config)) {
  const entry = config[key] || {};
  const htmlPath = path.join(tplDir, `${key}.html`);
  if (!existsSync(htmlPath)) {
    warnings.push(`config key '${key}' has no html file (${key}.html) — skipped`);
    continue;
  }
  emitTemplate({
    key,
    kind: "document",
    label: entry.label ?? null,
    category: entry.category ?? null,
    comment: entry._comment ?? null,
    html: readFileSync(htmlPath, "utf8"),
    schema: fieldSchema(entry.vars),
  });
  docCount++;
}

// warn for html files with no config entry
for (const f of readdirSync(tplDir).filter((f) => f.endsWith(".html"))) {
  const key = f.replace(/\.html$/, "");
  if (!(key in config)) warnings.push(`html '${f}' has no config entry — not seeded as document`);
}

// 2) partials (kind='partial', no config/schema)
if (existsSync(partialsDir)) {
  for (const f of readdirSync(partialsDir).filter((f) => f.endsWith(".html"))) {
    const key = f.replace(/\.html$/, "");
    emitTemplate({
      key,
      kind: "partial",
      label: key,
      category: "partial",
      comment: null,
      html: readFileSync(path.join(partialsDir, f), "utf8"),
      schema: [],
    });
    partialCount++;
  }
}

// 3) link current_version_id (idempotent)
out.push("-- link head -> version 1");
out.push(`UPDATE document_templates dt
SET current_version_id = v.id
FROM document_template_versions v
WHERE v.template_id = dt.id AND v.version_no = 1
  AND (dt.current_version_id IS DISTINCT FROM v.id);`);
out.push("");

writeFileSync(outFile, out.join("\n"), "utf8");
console.log(`Wrote ${outFile}: ${docCount} documents + ${partialCount} partials.`);
if (warnings.length) {
  console.log("Warnings:");
  for (const w of warnings) console.log("  - " + w);
}
